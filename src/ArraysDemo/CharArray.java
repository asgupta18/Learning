package ArraysDemo;

public class CharArray {
    public static void main(String[] args) {
        char [] ch = new char[5];

        ch[0] = 'a';
        ch[1]= 'b';
        ch[2]= 'c';
        ch[3]= 'd';
        ch[4]= 'e';

        for(int i = 0; i <5;i ++)
        {
            System.out.println(ch[i]);
        }
    }
}
